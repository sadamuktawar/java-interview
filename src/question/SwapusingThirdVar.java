package question;

public class SwapusingThirdVar {
public static void main(String[] args) {

	int num1=10;
int num2=20;
System.out.println("Before Swaping No");

System.out.println(num1);
System.out.println(num2);
    



num1=num1+num2;//30
    num2=num1-num2;//10
    num1=num1-num2;//20
    System.out.println("After Swaping No");
    System.out.println(num1);
    System.out.println(num2);

    

	
}
}
